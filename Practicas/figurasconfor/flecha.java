import java.util.*;
public class flecha {
    public static void main(String[] args) {
        Scanner s=new Scanner(System.in);
        int t=s.nextInt();
        for(int i=1; i<=t;i++){
           for(int n=1;n<=t;n++){
            System.out.println("*");
            n++;
        } 
        i++;
    }
    }
    
}